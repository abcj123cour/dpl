#' A square function
#'
#' takes a numeric value and squares it
#'
#' @param x a numeric value
#' @return The square of x
#' @export



square <- function (x){
  return (x^2)
}

#' cube a number
#'
#' takes a numeric value and cubes it
#'
#' @param Number to be cubed
#' @return the cube of the input
#' @export

cube <- function (x){
  return (x^3)
}
